package ForLoopExamples; // Package declaration, ensure your file is in the correct folder

public class Loop { // Class name starts with an uppercase letter, as per Java conventions
    public static void main(String[] args) { // Entry point of the program
        for (int counter = 0; counter < 1; counter = counter + 1) { // Iterates 10 times
            System.out.println("Hello World"); // Prints "Hello World" on each iteration
        }
    }
}

